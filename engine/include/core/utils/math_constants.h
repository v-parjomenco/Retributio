#pragma once

namespace core::utils {

    // --------------------------------------------------------------------------------------------
    // Математические константы (double precision для точности compile-time вычислений).
    // --------------------------------------------------------------------------------------------

    inline constexpr double kPi = 3.14159265358979323846;
    inline constexpr double kTwoPi = 6.28318530717958647692;
    inline constexpr double kHalfPi = 1.57079632679489661923;

    // --------------------------------------------------------------------------------------------
    // Конверсия градусы ↔ радианы.
    // --------------------------------------------------------------------------------------------
    // КОНТРАКТ:
    //  - Внутри движка (ECS components, game logic) угловые величины хранятся в ГРАДУСАХ.
    //  - Конверсия в радианы происходит ТОЛЬКО перед вызовом sin/cos/atan2 (RenderSystem).
    //  - SFML API принимает градусы (sf::Sprite::setRotation, sf::View::setRotation).
    //
    // ОБОСНОВАНИЕ:
    //  - Градусы удобнее для геймдизайна (90° = прямой угол, 180° = разворот).
    //  - Градусы удобнее для отладки (rotation=45.0 vs rotation=0.785398).
    //  - Конверсия deg→rad — одно умножение на константу (~0.5 CPU cycle), что пренебрежимо
    //    по сравнению со стоимостью sin/cos (~20-50 cycles).
    //  - В 4X стратегии rotation дискретный (8 направлений) или статичный — sin/cos вызывается
    //    редко (только в RenderSystem для видимых спрайтов с rotation ≠ 0).
    //
    // ПРОИЗВОДИТЕЛЬНОСТЬ:
    //  - Hot-path (RenderSystem): rotation * kDegToRad перед sin/cos — ~1-2% overhead от sin/cos.
    //  - Альтернатива (радианы везде) требует конверсий при загрузке JSON, отладке, экспорте —
    //    больше конверсий, не меньше.
    // --------------------------------------------------------------------------------------------

    inline constexpr float kDegToRad = static_cast<float>(kPi / 180.0);
    inline constexpr float kRadToDeg = static_cast<float>(180.0 / kPi);

    // --------------------------------------------------------------------------------------------
    // Вспомогательные функции (inline constexpr для zero-cost в Release).
    // --------------------------------------------------------------------------------------------

    /// Конвертирует градусы в радианы.
    /// Используется в местах, где нужна явная семантика (например, при загрузке углов из JSON).
    [[nodiscard]] inline constexpr float degToRad(const float degrees) noexcept {
        return degrees * kDegToRad;
    }

    /// Конвертирует радианы в градусы.
    /// Используется редко (например, при вычислении угла через atan2 и сохранении в градусах).
    [[nodiscard]] inline constexpr float radToDeg(const float radians) noexcept {
        return radians * kRadToDeg;
    }

} // namespace core::utils