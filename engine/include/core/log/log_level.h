// ================================================================================================
// File: core/log/log_level.h
// Purpose: Defines logging severity levels for the engine-wide logging subsystem.
// Used by: core/log/logging.h, core/log/log_macros.h, diagnostics tools.
// ================================================================================================
#pragma once

#include <cstdint>
#include <string_view>

namespace core::log {

    /**
     * @brief Уровни важности сообщений логирования.
     *
     * ВАЖНО (правило производительности для 4X):
     *  - В Release по умолчанию пишем только Info+.
     *  - Всё, что может оказаться в hot-path (внутри кадра, в цикле по юнитам/тайлам/сущностям,
     *    частые метрики вроде FPS, подробности ScalingSystem и т.п.)
     *    НЕ имеет права жить на Info/Warning.
     *    Для такого — максимум Trace (или Debug только при жёстком rate-limit).
     *  - Постоянные метрики во время разработки предпочтительно выводить в debug overlay /
     *    профайлер, а не в лог-файлы.
     *
     * Иерархия (от "самый болтливый" к "самый важный"):
     *  - Trace    — покадровая/пошаговая трассировка, детали циклов по сущностям,
     *               временные профилировочные метки; потенциально очень шумно;
     *               в Release обычно отключено/вырезано макросами.
     *  - Debug    — диагностика для разработчика, но НЕ спам: события "иногда"
     *               (редко, rate-limited), без сообщений в tight loop;
     *               используется для понимания причин поведения системы.
     *  - Info     — редкие “сюжетные” события и milestones: старт/остановка,
     *               загрузка ресурсов/конфигов, создание мира/сессии, итоговые сводки;
     *               должно быть безопасно оставить включённым в Release.
     *  - Warning  — потенциальная проблема, но игра/симуляция продолжается;
     *               сообщение должно помогать найти причину.
     *  - Error    — ошибка: часть функциональности не работает корректно;
     *               требуется внимание, но процесс может жить.
     *  - Critical — фатальная ошибка: продолжать нельзя (далее обычно panic/exit).
     *
     * Рекомендация по сборкам:
     *  - Debug: Trace/Debug включаются по необходимости,
     *    но обязателен контроль частоты (rate-limit) на шумных местах.
     *  - Release: Info+ (Trace/Debug выключены),
     *    чтобы логирование не влияло на производительность симуляции.
     */
    enum class Level : std::uint8_t {
        Trace    = 0,
        Debug    = 1,
        Info     = 2,
        Warning  = 3,
        Error    = 4,
        Critical = 5
    };

    /**
     * @brief Возвращает человеко-читаемое имя уровня логирования.
     *
     * Примеры:
     *  - Level::Info     -> "INFO"
     *  - Level::Warning  -> "WARNING"
     *
     * Реализация находится в .cpp, чтобы не плодить зависимости в заголовке.
     */
    std::string_view toString(Level level) noexcept;

} // namespace core::log